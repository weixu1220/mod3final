const mongoose = require('mongoose')
const Schema = mongoose.Schema
const adminSchema = new Schema({
    firstname: {
        type: String,
        required: true,
    },
    lastname: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
})
const Admin = mongoose.model('Admin', adminSchema)

module.exports = Admin